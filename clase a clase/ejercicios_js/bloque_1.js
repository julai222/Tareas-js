// 1 Declara una variable nombre y guarda tu nombre. Muestra el valor en consola.
let nombre = "Julai";
console.log("Mi nombre es:", nombre);

// 2 Declara dos variables numericas y muestra la suma en consola.
let numero1 = 5;
let numero2 = 7;
console.log("La suma es:", numero1 + numero2);

// 3️ Crea una variable booleana (true o false) y muéstrala.
let esProgramadora = true;
console.log("¿Soy programadora?", soyProgramadora);

// 4️ Cambia el valor de una variable y muestra el antes y el despues.
let color = "rojo";
console.log("Antes el color era:", color);
color = "azul";
console.log("Ahora el color es:", color);

// 5️ Usa typeof para imprimir el tipo de cada variable.
console.log("Tipo de 'nombre':", typeof nombre);
console.log("Tipo de 'numero1':", typeof numero1);
console.log("Tipo de 'soyProgramadora':", typeof soyProgramadora);
console.log("Tipo de 'color':", typeof color);
